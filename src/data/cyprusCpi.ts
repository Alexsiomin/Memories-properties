export const CPI_YEARS = Object.keys(CYPRUS_CPI)
  .map(Number)
  .sort((a, b) => a - b);

export const getCpi = (year: number, monthIndex: number): number | undefined =>
  CYPRUS_CPI[year]?.[monthIndex];

// Returns all { year, monthIndex } pairs that have a published CPI value.
export const getCpiOptions = (): { year: number; monthIndex: number }[] => {
  const out: { year: number; monthIndex: number }[] = [];
  for (const year of CPI_YEARS) {
    CYPRUS_CPI[year].forEach((_, monthIndex) => out.push({ year, monthIndex }));
  }
  return out;
};

// Latest published month (used as the default "sale" reference).
export const getLatestCpiDate = (): { year: number; monthIndex: number } => {
  const year = CPI_YEARS[CPI_YEARS.length - 1];
  return { year, monthIndex: CYPRUS_CPI[year].length - 1 };
};
